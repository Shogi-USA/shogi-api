package com.shogiusa.shogiapi.enums;

/**
 * Enum representing different types of tokens used in the application.
 * <p>
 * This enum can be expanded to include different types of tokens as needed.
 */
public enum TokenType {
    /**
     * Represents a bearer token, a type of access token used in
     * OAuth 2.0 and OpenID Connect protocols. Bearer tokens allow
     * access to resources and are often included in the Authorization
     * header of HTTP requests.
     */
    BEARER // Represents a bearer token
}
